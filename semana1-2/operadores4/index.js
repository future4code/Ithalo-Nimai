let valor1 = Number(prompt("Digite um número!"))
let valor2 = Number(prompt("Digite outro número"))

console.log("O primeiro número é maior que o segundo?", valor1 > valor2)
console.log("O primeiro número é igual ao segundo?", valor1 === valor2)
console.log("O primeiro número é divisivel pelo segundo?", valor1 % valor2 === 0)
console.log("O segundo número é divisivel pelo primeiro?", valor2 % valor1 === 0)
